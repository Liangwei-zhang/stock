const messages: Record<SupportedLocale, Record<TranslationKey, string>> = {
  'en-US': enMessages,
  'zh-CN': zhCNMessages,
  'zh-TW': zhTWMessages,
};

const antdLocales = {
  'en-US': enUS,
  'zh-CN': zhCN,
  'zh-TW': zhTW,
};

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? ''));
}

export function normalizeLocale(value?: string | null): SupportedLocale {
  const locale = String(value ?? '').trim().toLowerCase();
  if (locale.startsWith('zh-tw') || locale.startsWith('zh-hk') || locale.startsWith('zh-mo')) {
    return 'zh-TW';
  }
  if (locale.startsWith('zh')) {
    return 'zh-CN';
  }
  if (locale.startsWith('en')) {
    return 'en-US';
  }
  return 'en-US';
}

export function translateText(locale: SupportedLocale, key: TranslationKey, vars?: Record<string, string | number>) {
  const template = messages[locale][key] ?? messages['en-US'][key] ?? key;
  return interpolate(template, vars);
}

function resolveInitialLocale(userLocale?: string | null): SupportedLocale {
  if (userLocale) {
    return normalizeLocale(userLocale);
  }
  const stored = localStorage.getItem('app_locale');
  if (stored) {
    return normalizeLocale(stored);
  }
  return normalizeLocale(window.navigator.language);
}

interface I18nContextValue {
  locale: SupportedLocale;
  setLocale: (nextLocale: string) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
  formatNumber: (value: number, options?: Intl.NumberFormatOptions) => string;
  formatCurrency: (value: number, currency: string, options?: Intl.NumberFormatOptions) => string;
  formatDate: (value: string | number | Date, options?: Intl.DateTimeFormatOptions) => string;
  antdLocale: (typeof antdLocales)[SupportedLocale];
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const { user } = useAuth();
  const [locale, setLocaleState] = useState<SupportedLocale>(() => resolveInitialLocale(user?.locale));

  useEffect(() => {
    if (!user?.locale) return;
    const nextLocale = normalizeLocale(user.locale);
    setLocaleState(prev => prev === nextLocale ? prev : nextLocale);
  }, [user?.locale]);

  useEffect(() => {
    localStorage.setItem('app_locale', locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const setLocale = (nextLocale: string) => {
    setLocaleState(normalizeLocale(nextLocale));
  };

  const t = (key: TranslationKey, vars?: Record<string, string | number>) =>
    translateText(locale, key, vars);

  const formatNumber = (value: number, options?: Intl.NumberFormatOptions) =>
    new Intl.NumberFormat(locale, options).format(value);

  const formatCurrency = (value: number, currency: string, options?: Intl.NumberFormatOptions) =>
    new Intl.NumberFormat(locale, {
      style: 'currency',
      currency,
      ...options,
    }).format(value);

  const formatDate = (value: string | number | Date, options?: Intl.DateTimeFormatOptions) =>
    new Intl.DateTimeFormat(locale, options).format(new Date(value));

  return (
    <I18nContext.Provider value={{
      locale,
      setLocale,
      t,
      formatNumber,
      formatCurrency,
      formatDate,
      antdLocale: antdLocales[locale],
    }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return ctx;
}